
CREATE TABLE users (
  fname        varchar(25) not null,
  lname        varchar(25) not null,
  email        varchar(50) not null,
  username     varchar(25) not null,
  password     varchar(25) not null,
  userid       integer(5) not null,
  primary key (userid),
  unique (username)
);

CREATE TABLE follow (
  followerid    integer(5),
  followeeid    integer(5),
  followid      integer(5) not null,
  primary key (followid),
  foreign key (followerid) references users(userid),
  foreign key (followeeid) references users(userid)
);

CREATE TABLE feed (
  feedid         integer(5) not null,
  fmodificationdate     datetime not null,
  feedtext        text,
  feedownerid     integer(5) not null,
  primary key (feedid),
  foreign key (feedownerid) references users(userid)
);

CREATE TABLE comments (
  commentid       integer(5) not null,
  cmodificationdate    datetime not null,
  commenttext        text,
  commentownerid     integer(5) not null,
  feedforid          integer(5) not null,
  primary key (commentid),
  foreign key (commentownerid) references users(userid),
  foreign key (feedforid) references feed(feedid)
);

CREATE TABLE likes(
  likeid       integer(5) not null,
  feedlikeid   integer(5) not null,
  likerid      integer(5) not null,
  primary key (likeid),
  foreign key (feedlikeid) references feed(feedid),
  foreign key (likerid) references users(userid)
);

CREATE TABLE retweet (
  feedtweetid   integer(5) not null,
  tweetedid     integer(5) not null,
  tweeterid     integer(5) not null,
  retweetdate    datetime not null,
  primary key (feedtweetid),
  foreign key (tweetedid) references feed(feedid),
  foreign key (tweeterid) references users(userid)
);

